// フィルタリング関数
export const filterFacilities = (
  facilities: Facility[],
  filters: {
    types: string[];
    prefectures: string[];
    districts: string[];
    hasSaturday: boolean | null;
    capacityRange: string | null;
  }
): Facility[] => {
  return facilities.filter(facility => {
    // 施設タイプフィルター
    if (filters.types.length > 0 && !filters.types.includes(facility.type)) {
      return false;
    }

    // 都道府県フィルター
    if (filters.prefectures.length > 0) {
      if (!facility.prefecture) return false;

      // 都道府県IDとマッチングするために、prefectureフィールドを使用
      // facility.prefecture は '北海道' or '富山県' などの文字列
      // filters.prefectures は ['hokkaido', 'toyama'] などのIDの配列
      const prefectureMatches = filters.prefectures.some(prefId => {
        if (prefId === 'hokkaido' && facility.prefecture === '北海道') return true;
        if (prefId === 'toyama' && facility.prefecture === '富山県') return true;
        return false;
      });

      if (!prefectureMatches) return false;
    }

    // 市区町村フィルター
    if (filters.districts.length > 0 && facility.district && !filters.districts.includes(facility.district)) {
      return false;
    }

    // 土曜日対応フィルター
    if (filters.hasSaturday === true && facility.openingHours?.saturday === '未実施') {
      return false;
    }

    // 定員規模フィルター
    if (filters.capacityRange && facility.capacity) {
      if (filters.capacityRange === 'small' && facility.capacity > 10) {
        return false;
      }
      if (filters.capacityRange === 'medium' && (facility.capacity <= 10 || facility.capacity > 30)) {
        return false;
      }
      if (filters.capacityRange === 'large' && facility.capacity <= 30) {
        return false;
      }
    }

    return true;
  });
};