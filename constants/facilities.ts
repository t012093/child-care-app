  lat: number;
  lng: number;
  phone?: string;
  email?: string;
  description?: string;
  rating: number;
  imageUrl: string;
  distance?: number; // in km
  // 地域関連フィールド
  prefecture?: string; // 都道府県（例: '北海道', '富山県'）
  district?: string; // 市区町村（例: '中央区', '富山市', '高岡市'）
